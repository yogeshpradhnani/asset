const  {mongoose,Schema }= require('mongoose')

const userSchema = Schema({

    name: {
        type: String,
        required: true,
        minlength: 3,
        maxlength: 50
    },
    email: {
        type: String,
        required: true,
        unique: true,
        match: /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/
    },
    password: {
        type: String,
        required: true,
        minlength: 8,
        select: false
    },
},{timestamps:true}
)

module.exports = mongoose.model('User', userSchema)